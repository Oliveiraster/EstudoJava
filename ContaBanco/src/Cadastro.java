public class Cadastro {
    int Numero ;
    String Agencia ;
    String NomeCliente ;
    double Saldo ;

    public void setNumero(int value)    {
        Numero = value;
    }
    public void setAgencia(String value)    {
        Agencia = value;
    }
    public void setNomeCliente(String value)    {
        NomeCliente = value;
    }
    public void setSaldo(double value)    {
        Saldo = value;
    }
}
